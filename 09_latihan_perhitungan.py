# program konversi celcius ke satu yang lain

var_cel = float(input('masukkan nilai celcius :'))
print('suhu yang anda masukkan :', var_cel)

# reamur
hasil = (4/5) * var_cel
print('hasil konveri ke reamur :', hasil)

# farenheit
hasil = ((9/5) * var_cel) + 32
print('hasil konveri ke farenheit :', hasil)

# kelvin
hasil = var_cel + 273
print('hasil konveri ke kelvin :', hasil)